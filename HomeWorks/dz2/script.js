// * ЗАДАЧА 16 – Время до конца дня
// 1.  3апpосите y пользователя текущее время (часы и минуты) и выведете, 
// сколько часов и минут осталось до следующего дня.

let hours = prompt("Текущий час (0-23):");
let minutes = prompt("Текущие минуты (0-59):");
let rHours = 23 - hours;
let rMinutes = 59 - minutes;
if (rMinutes < 0) 
{
    rMinutes += 60;
    --rHours;
}
alert(`До конца дня: ${rHours} ч ${rMinutes} мин`);
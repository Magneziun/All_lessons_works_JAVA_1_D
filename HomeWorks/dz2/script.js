// * ЗАДАЧА 3 – високосный год
// 1. Запросить у пользователя год и проверить, високосный он или нет.
//  Високосный год либо кратен 400, либо кратен 4 и при этом не кратен 100. Результат вывести с помощью alert

let year = parseInt(prompt("Введите год:"));
let isLeap;
if ((year % 400 === 0) || (year % 4 === 0 && year % 100 !== 0)) 
{
    isLeap = true;
}
else 
{
    isLeap = false;
}
if (isLeap) 
{
    alert(`${year} - високосный год`);
}
else 
{
    alert(`${year} - не висококсный год`);
}

// * ЗАДАЧА 6 – скидка
// 1. Запросить у пользователя сумму покупки и вывести сумму к оплате со скидкой:
//  от 200 до 300 – скидка будет 3%, от 301 до 500 – 5%, от 501 и выше – 7%. Результат вывести с помощью alert.

let purchase = prompt("Сумма покупки: ");
let discount;
if (purchase >= 501) 
{
    discount = 0.07;
} else if (purchase >= 301) 
{
    discount = 0.05;
} else if (purchase >= 200) 
{
    discount = 0.03;
} else {
    discount = 0;
}
let total = purchase * (1 - discount);
alert(`Сумма к оплате со скидкой: ${total.toFixed(2)}`);

